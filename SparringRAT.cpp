HMODULE GetCurrentModule()
{ 
	HMODULE hModule = NULL;
	GetModuleHandleEx(
		GET_MODULE_HANDLE_EX_FLAG_FROM_ADDRESS,
		(LPCTSTR)GetCurrentModule,
		&hModule);

	return hModule;
}

bool IsCurrentProcessRundll32() 
{
	//kiểm tra xem process co phải được chạy bằng rundll32
	HANDLE hProcess = GetCurrentProcess();
	wchar_t exePath[MAX_PATH];
	if (GetModuleFileNameExW(hProcess, NULL, exePath, MAX_PATH))
	{
		wchar_t* exeName = PathFindFileNameW(exePath);

		return _wcsicmp(exeName, L"rundll32.exe") == 0;
	}

	return false; 
}

std::wstring GetDllNameFromHandle(HMODULE hModule) 
{
	wchar_t moduleName[MAX_PATH];
	if (GetModuleFileNameW(hModule, moduleName, sizeof(moduleName) / sizeof(wchar_t))) {
		return std::wstring(moduleName);
	}
	return std::wstring();
}

